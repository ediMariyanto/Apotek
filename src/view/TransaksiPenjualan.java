    private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
        if (JOptionPane.showConfirmDialog(this,
                "Yakin ingin reset?", "Reset", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
            resetForm();
        }
    }//GEN-LAST:event_btnResetActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if (validasiSimpan()) {
            CtrTransaksi ct;
            for (int i = 0; i < detail.getRowCount(); i++) {
                ct = new CtrTransaksi();
                ct.setKodeBarang(detail.getValueAt(i, 0).toString());
                ct.setHarga(Double.parseDouble(detail.getValueAt(i, 2).toString()));
                ct.setJumlah(Integer.parseInt(detail.getValueAt(i, 3).toString()));
                ct.setTgl(jxdTanggal.getDate());
                ct.setNomorNota(txtNoNota.getText());
                ct.setReferensi(txtPenjual.getText());
                ct.tambahTransaksiJual();
            }
            JOptionPane.showMessageDialog(this,
                    "Transaksi Jual berhasil disimpan", "Berhasil",
                    JOptionPane.INFORMATION_MESSAGE);
            resetForm();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void resetForm() {
        clearTxt();
        for (int i = detail.getRowCount() - 1; i >= 0; i--) {
            detail.removeRow(i);
        }
        jxdTanggal.setDate(new Date());
        txtNoNota.setText("");
    }

    private boolean validasiSimpan() {
        boolean valid = true;
        lblPesanDetail.setText("");
        lblPesanNoNota.setText("");
        if (detail.getRowCount() == 0) {
            lblPesanDetail.setText("Detail belum diisi. ");
            valid = false;
        }
        if (txtNoNota.getText().equals("")) {
            lblPesanNoNota.setText("Nomor Nota belum diisi. ");
            valid = false;
        }
        return valid;
    }

    private void klikKanan(java.awt.event.MouseEvent e) {
        int r = tblTransaksi.rowAtPoint(e.getPoint());
        if (r >= 0 && r < tblTransaksi.getRowCount()) {
            tblTransaksi.setRowSelectionInterval(r, r);
        } else {
            tblTransaksi.clearSelection();
        }
        int rowindex = tblTransaksi.getSelectedRow();
        if (rowindex < 0) {
            return;
        }
        if (e.getComponent() instanceof JTable) { //only right click -> (e.isPopupTrigger() && e.getComponent() instanceof JTable)
            JPopupMenu popup = new JPopupMenu();
            JMenuItem deleteItem = new JMenuItem("Delete");
            deleteItem.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    int rowindex = tblTransaksi.getSelectedRow();
                    detail.removeRow(rowindex);
                    setModelTbl();
                }
            });
            popup.add(deleteItem);
            popup.show(e.getComponent(), e.getX(), e.getY());
        }
    }

    private boolean validasiTambah() {
        boolean valid = true;
        lblPesanBarang.setText("");
        lblPesanHarga.setText("");
        lblPesanJumlah.setText("");
        if (txtKodeBarang.getText().equals("")) {
            lblPesanBarang.setText("Barang belum dipilih. ");
            valid = false;
        }
        if (!Parameter.isDouble(txtHarga.getText())) {
            lblPesanHarga.setText("Harga tidak valid. ");
            valid = false;
        }
        if (!Parameter.isInteger(txtJumlah.getText())) {
            lblPesanJumlah.setText("Jumlah tidak valid. ");
            valid = false;
        }
        return valid;
    }

    private void setModelTbl() {
        tblTransaksi.setModel(detail);
        tblTransaksi.getColumnModel().getColumn(2).setCellRenderer(Parameter.rightRenderer());
        tblTransaksi.getColumnModel().getColumn(3).setCellRenderer(Parameter.rightRenderer());
        tblTransaksi.getColumnModel().getColumn(4).setCellRenderer(Parameter.rightRenderer());
        tblTransaksi.setEnabled(false);
    }

    private void clearTxt() {
        txtJumlah.setText("");
        txtKodeBarang.setText("");
        txtMerk.setText("");
        txtHarga.setText("");
    }

