    private void btnPilihBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihBarangActionPerformed
        PilihBarang pb = new PilihBarang();
        if (pb.getBarang() != null) {
            txtKodeBarang.setText(pb.getBarang().getKode() + pb.getBarang().getKodeDetail());
            txtMerk.setText(pb.getBarang().getMerk());
            txtHarga.setText(Double.toString(pb.getBarang().getHarga()));
            txtJumlah.setText("1");
        }
    }//GEN-LAST:event_btnPilihBarangActionPerformed

